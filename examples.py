# ф-ция global <название переменной> - берет переменные из глобального пространства имен, а если там нет такой переменной, 
# то создает ее
# ф-ция nonlocal <название переменной> - берет переменные из самого ближайшего пространства имен, где есть такое название.
# порядок поиска имени переменной не связан со стеком вызовов. Порядок поиска имени переменной связан лишь с порядком вложенности друг в друга областей видимости.
